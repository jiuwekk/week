void ipark_calc(IPARK_OBJ *v)
{
    v->Alpha = v->Ds*cos(v->Angle) - v->Qs*sin(v->Angle);
    v->Beta  = v->Ds*sin(v->Angle) + v->Qs*cos(v->Angle);
}


